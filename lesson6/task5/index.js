function squareArray(numbers) {
    if (!Array.isArray(numbers)) {
        return null;
    }
    let arrNumbers = [];
    for (let i = 0; i <= numbers.length; i++) {
        Math.pow(i, 2);
        arrNumbers.push(i);
    }
    return arrNumbers;
}
